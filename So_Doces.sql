CREATE TABLE Cargos (
    ID_Cargo INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nome_Cargo VARCHAR(50) NOT NULL
);

CREATE TABLE Clientes (
    ID_Cliente INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nome VARCHAR(100) NOT NULL,
    CPF VARCHAR(11) UNIQUE,
    Telefone VARCHAR(20)
);

CREATE TABLE Formas_Pagamento (
    ID_Forma_Pagto INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Descricao VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE Produtos (
    ID_Produto INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nome VARCHAR(150) NOT NULL,
    Codigo_Barras VARCHAR(50) UNIQUE,
    Preco_Venda DECIMAL(10, 2) NOT NULL,
    Categoria VARCHAR(50)
);

CREATE TABLE Funcionarios (
    ID_Funcionario INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nome VARCHAR(100) NOT NULL,
    Login VARCHAR(50) NOT NULL UNIQUE,
    Senha VARCHAR(255) NOT NULL,
    ID_Cargo INT NOT NULL,
    CONSTRAINT FK_Funcionarios_Cargos 
    	FOREIGN KEY (ID_Cargo) 
    	REFERENCES Cargos(ID_Cargo)
);

CREATE TABLE Lotes (
    ID_Lote INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_Produto INT NOT NULL,
    Codigo_Lote VARCHAR(50) NOT NULL,
    Data_Validade DATE NOT NULL,
    Quantidade_Inicial INT NOT NULL,
    Quantidade_Atual INT NOT NULL,
    CONSTRAINT FK_Lotes_Produtos 
    	FOREIGN KEY (ID_Produto) 
    	REFERENCES Produtos(ID_Produto)
);

CREATE TABLE Vendas (
    ID_Venda INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Data_Hora TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    Valor_Total DECIMAL(10, 2) NOT NULL,
    ID_Funcionario INT NOT NULL,
    ID_Forma_Pagto INT NOT NULL,
    ID_Cliente INT NULL,
    CONSTRAINT FK_Vendas_Funcionarios 
    	FOREIGN KEY (ID_Funcionario) 
    	REFERENCES Funcionarios(ID_Funcionario),
    CONSTRAINT FK_Vendas_Formas_Pagamento 
    	FOREIGN KEY (ID_Forma_Pagto) 
    	REFERENCES Formas_Pagamento(ID_Forma_Pagto),
    CONSTRAINT FK_Vendas_Clientes 
    	FOREIGN KEY (ID_Cliente) 
    	REFERENCES Clientes(ID_Cliente)
);

CREATE TABLE Itens_Venda (
    ID_Item INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_Venda INT NOT NULL,
    ID_Lote INT NOT NULL,
    Quantidade INT NOT NULL,
    Preco_Praticado DECIMAL(10, 2) NOT NULL,
    CONSTRAINT FK_Inter_Venda_Vendas 
    	FOREIGN KEY (ID_Venda) 
    	REFERENCES Vendas(ID_Venda) 
    	ON DELETE CASCADE,
    CONSTRAINT FK_Itens_Venda_Lotes 
    	FOREIGN KEY (ID_Lote) 
    	REFERENCES Lotes(ID_Lote)
);

CREATE TABLE Perdas_Estoque (
    ID_Perda INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_Lote INT NOT NULL,
    ID_Funcionario INT NOT NULL,
    Quantidade INT NOT NULL,
    Motivo VARCHAR(50) NOT NULL,
    Data_Hora TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_Perdas_Estoque_Lotes 
    	FOREIGN KEY (ID_Lote) 
    	REFERENCES Lotes(ID_Lote),
    CONSTRAINT FK_Perdas_Estoque_Funcionarios 
    	FOREIGN KEY (ID_Funcionario) 
    	REFERENCES Funcionarios(ID_Funcionario)    
);

