-- Function 2: sp_AdicionarItemVenda
CREATE OR REPLACE FUNCTION sp_AdicionarItemVenda(
    p_ID_Venda INT,
    p_ID_Lote INT,
    p_Quantidade INT,
    p_Preco_Praticado DECIMAL(10,2)
) 
RETURNS VARCHAR 
LANGUAGE plpgsql 
AS $$
DECLARE
    v_qtd_atual INT;
    v_subtotal DECIMAL(10,2);
BEGIN
    SELECT Quantidade_Atual INTO v_qtd_atual
    FROM Lotes WHERE ID_Lote = p_ID_Lote;

    IF v_qtd_atual >= p_Quantidade THEN
        INSERT INTO Itens_Venda (ID_Venda, ID_Lote, Quantidade, Preco_Praticado)
        VALUES (p_ID_Venda, p_ID_Lote, p_Quantidade, p_Preco_Praticado);

        UPDATE Lotes
        SET Quantidade_Atual = Quantidade_Atual - p_Quantidade
        WHERE ID_Lote = p_ID_Lote;

        v_subtotal := p_Quantidade * p_Preco_Praticado;
        
        UPDATE Vendas
        SET Valor_Total = Valor_Total + v_subtotal
        WHERE ID_Venda = p_ID_Venda;
        
        RETURN 'Item adicionado e venda atualizada com sucesso!';
    ELSE
        RETURN 'Erro: Estoque insuficiente para realizar a venda deste lote.';
    END IF;
END;
$$;

SELECT sp_AdicionarItemVenda(1, 3, 3, 10.00);

SELECT id_venda, valor_total FROM vendas WHERE id_venda = 1;


